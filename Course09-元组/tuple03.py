"""
打包和解包操作
"""

'''
当我们把多个用逗号分隔的值赋给一个变量时，多个值会打包成一个元组类型；
当我们把一个元组赋值给多个变量时，元组会解包成多个值然后分别赋给对应的变量，如下面的代码所示。
'''

# 打包
a = 1, 10, 100
print(type(a), a)    # <class 'tuple'> (1, 10, 100)

# 解包
i, j, k = a
print(i, j, k)       # 1 10 100

a, b, *c = range(1, 10)
print(a, b, c)  # 1 2 [3, 4, 5, 6, 7, 8, 9]
a, *b, c = range(1, 10)
print(a, b, c)  # 1 [2, 3, 4, 5, 6, 7, 8] 9
a, b, c = [1, 10, 100]
print(a, b, c)
*a, b, c = 'hello'
print(a, b, c)  # ['h', 'e', 'l'] l o
